package ch03;

public class Ch03_문자 {
    public static void main(String[] args) {
        char a1 = 'a'; //문자는 한따옴표로
        char a2 = 97;
        char a3 = '\u0061';  //는 유니코드를 나타낸다.
        System.out.println(a1);
        System.out.println(a2);
        System.out.println(a3);
        //a의 세가지 다른 표현
    }
}
